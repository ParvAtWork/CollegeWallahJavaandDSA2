// =======================================
// Topic: Java Learning Flow (From Beginning)
// Format: IntelliJ-Friendly Notes
// =======================================

        /*
         * ---------------------------------------
         * Step 1: Basics of Java
         * ---------------------------------------
         * - Introduction to Java, JDK, JVM, IDE setup
         * - First program: Hello World
         * - Data types: int, float, char, boolean, String
         * - Variables and constants
         * - Input/Output: Scanner class, System.out
         */

        /*
         * ---------------------------------------
         * Step 2: Operators & Expressions
         * ---------------------------------------
         * - Arithmetic operators (+, -, *, /, %)
         * - Relational operators (==, !=, >, <)
         * - Logical operators (&&, ||, !)
         * - Assignment operators (+=, -=)
         * - Increment/Decrement (++ / --)
         */

        /*
         * ---------------------------------------
         * Step 3: Control Flow
         * ---------------------------------------
         * - Conditional statements: if, else if, else
         * - Switch case
         * - Loops:
         *   → for loop
         *   → while loop
         *   → do-while loop
         *   → enhanced for loop (for-each)
         */

        /*
         * ---------------------------------------
         * Step 4: Arrays
         * ---------------------------------------
         * - 1D Arrays: declaration, initialization, traversal
         * - Operations: sum, max, min, search
         * - 2D Arrays: matrix input/output, traversal
         * - Dry runs and edge cases
         */

        /*
         * ---------------------------------------
         * Step 5: Strings
         * ---------------------------------------
         * - String basics: immutable nature
         * - Methods: length(), charAt(), substring(), equals(), compareTo()
         * - StringBuilder (mutable strings)
         * - Problems: reverse string, palindrome check
         */

        /*
         * ---------------------------------------
         * Step 6: Object-Oriented Programming (OOP)
         * ---------------------------------------
         * - Classes and Objects
         * - Constructors
         * - Methods (static vs non-static)
         * - Access modifiers (public, private, protected)
         * - Inheritance
         * - Polymorphism (overloading, overriding)
         * - Abstraction (abstract classes, interfaces)
         * - Encapsulation
         */

        /*
         * ---------------------------------------
         * Step 7: Exception Handling
         * ---------------------------------------
         * - try, catch, finally
         * - throw vs throws
         * - Common exceptions: NullPointerException, ArrayIndexOutOfBounds
         */

        /*
         * ---------------------------------------
         * Step 8: Collections Framework
         * ---------------------------------------
         * - Why Collections (arrays ki limitations)
         * - List (ArrayList, LinkedList)
         * - Set (HashSet, TreeSet)
         * - Map (HashMap, TreeMap)
         * - Iterators and for-each traversal
         */

        /*
         * ---------------------------------------
         * Step 9: Advanced Topics
         * ---------------------------------------
         * - Generics
         * - File Handling (read/write files)
         * - Multithreading basics
         * - Streams API
         * - Lambda expressions
         */

        /*
         * ---------------------------------------
         * Step 10: Data Structures & Algorithms (DSA)
         * ---------------------------------------
         * - Stacks, Queues, Linked Lists
         * - Recursion
         * - Searching (linear, binary)
         * - Sorting (bubble, selection, insertion, quick, merge)
         * - Trees, Graphs
         * - Dynamic Programming
         */

        /*
         * ---------------------------------------
         * Hinglish Summary:
         * ---------------------------------------
         * Flow hamesha Basics → Arrays → Strings → OOP → Collections → Advanced → DSA hota hai.
         * Arrays aur Strings ke baad hi Collections seekhte ho.
         * Collections ek powerful toolkit hai jo arrays ki limitations ko solve karta hai.
         */

